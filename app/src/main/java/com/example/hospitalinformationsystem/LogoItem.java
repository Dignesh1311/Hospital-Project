package com.example.hospitalinformationsystem;

public class LogoItem {
    String logoname;
    int logo;

    public LogoItem(String logoname, int logo) {
        this.logoname = logoname;
        this.logo = logo;
    }

    public String getLogoname() {
        return logoname;
    }

    public int getLogo() {
        return logo;
    }
}
